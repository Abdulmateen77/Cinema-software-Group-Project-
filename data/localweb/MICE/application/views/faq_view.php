<h2>How can I add a booking for a performance?</h2>
<p>To create a new booking, simply navigate to the 'Booking' table, then click on 'Add booking' and you can add the relevant details for your booking there.
<h2>How can I amend an existing booking which I have previously created?</h2>
<p>In the 'Booking' table, locate the relevant booking, click on 'edit' and you can change various aspects of your booking here. Once complete, click 'Update' and you'll receive a message, which notifies you that your update has been successful.
<h2>How can I cancel a user's membership?</h2>
<p>To stop their membership, simply navigate to the 'Member' table, locate the relevant member, and click on 'Delete', which is located to the right of their membership details.
<h2>How can I add information about a new film?</h2>
<p>To add a new film to the system, go to the 'Films' table, click on 'Add film' then complete the relevant details about that film to add it to the database.
<h2>How can I view membership information?</h2>
<p>To view information regarding members, you can view this in the 'Members' table, where all the relevant information is displayed, including their current status. You can also edit a member's information or cancel their membership on the right hand side of this page.
